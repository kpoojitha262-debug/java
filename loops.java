//For loop
class ForLoop {
    public static void main(String args[]) {
        for(int i = 1; i <= 5; i++) {
            System.out.println(i);
        }
    }
}
//While 
class While {
 public static void main(String[] args) {
   int i = 1;
        while(i <= 5) {
            System.out.println(i);
            i++;
     }
   }
}
//Do-While
class DoWhile{
public static void main (String[] args){
int i = 1;
        do {
            System.out.println(i);
            i++;
        } while(i <= 5);
    }
}
